namespace App\Observers;

use App\Models\Order;

class OrderObserver
{
    /**
     * Handle the Order "created" event.
     */
    public function created(Order $order): void
    {
        // Generate the invoice ID
        $dateTime = now()->format('Ymd');
        $orderId = $order->id;
        $userId = $order->user_id;

        $invoiceId = $dateTime . $orderId . $userId;

        // Update the order with the generated invoice ID
        $order->invoice = $invoiceId;
        $order->save();
    }

